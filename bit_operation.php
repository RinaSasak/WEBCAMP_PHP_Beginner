<?php

$i = random_int(0, 255);
$j = random_int(0, 255);

//AND（論理積）：どちらの入力も1のときのみ1、それ以外は0
$k = $i & $j;
printf("%x & %x = %x \n", $i, $j, $k);

//OR（論理和）：入力のどちらか或いは両方が1の場合は1、どちらも0の場合だけ0
$k = $i | $j;
printf("%x | %x = %x \n", $i, $j, $k);

//XOR（排他的論理和）：入力のAとBで値が違うときは1、同じときは0
$k = $i ^ $j;
printf("%x ^ %x = %x \n", $i, $j, $k);

//NOT（論理否定）：入力が1のときは0、入力が0のときは1
$k = ~$i;
printf("~%x = %x \n", $i, $k);
//桁が大きい　key:二の補数、符号bit